class Utils {
  static String URLEncode(String input) {
    String encoded = '';
    for (var char in input.split("")) {
      if (char == ' ') {
        encoded += "%20";
      } else if (char == '/') {
        encoded += "%2F";
      } else {
        encoded += char;
      }
    }
    return encoded;
  }

  static String getWordOfTheDay() {
    var numberOfDays = getDate();
    return words[numberOfDays];
  }

  static int getDate() {
    List<int> daysInMonths = [
      31, // January
      29, // February
      31, // March
      30, // April
      31, // May
      30, // June
      31, // July
      31, // August
      30, // September
      31, // October
      30, // November
      31 // December
    ];

    DateTime now = DateTime.now();
    // Calculate the position in the year
    int position = 0;
    for (int i = 0; i < now.month - 1; i++) {
      position += daysInMonths[i];
    }
    position += now.day;
    return 0;
  }

